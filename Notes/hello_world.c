// C is a compiled language
// To use the most basic function, printf, which is defined in the stdio.h header file, 
// we must have this in our first line of code:
#include <stdio.h>

// Next, the first code that will run resides in the main function.
void main() {
    printf("Hello, World!");
}

// This function returns nothing, so, similar to Java, we specify "void".
// Specifying "int" means we will return an integer